# Write a Python program to find the addition and subtraction of two numbers.
# Ask the user to input two numbers (num1 and num2). Given those two numbers, 
# add them together to find the output. Also, subtract the two numbers to find the output.

# Addition
print("Please input two numbers to add together:")
print("First input:")
num1 = int(input())

print("Second input:")
num2 = int(input())
print(num1, '+', num2, '=', num1 + num2)

# Subtraction
print("Please input two numbers to subtract:")
print("First input:")
num1 = int(input())

print("Second input:")
num2 = int(input())
print(num1, '-', num2, '=', num1 - num2)

